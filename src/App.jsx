function CodePreview({ code }) {
  const [copied, setCopied] = useState(false);

  const handleCopy = () => {
    navigator.clipboard.writeText(code);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <div style={{ position: "relative" }}>
      <button onClick={handleCopy} style={{
        position: "absolute", top: 12, right: 12, padding: "6px 12px",
        background: copied ? "rgba(34,197,94,0.15)" : "rgba(255,255,255,0.05)",
        border: "1px solid " + (copied ? "rgba(34,197,94,0.3)" : "rgba(255,255,255,0.1)"),
        borderRadius: 6, color: copied ? "#4ade80" : "#94a3b8", fontSize: '0.75rem', cursor: "pointer",
        fontFamily: "'JetBrains Mono', monospace", transition: 'all 0.2s', zIndex: 10
      }}>
        {copied ? "✓ Copied" : "Copy"}
      </button>
      <pre className="code-container">
        {code.split("\n").map((line, i) => {
          let color = "#9ca3af";
          if (line.startsWith(";")) color = "#4ade80"; // comments
          else if (line.includes("::")) color = "#f59e0b"; // hotkey
          else if (line.startsWith("Menu,")) color = "#60a5fa"; // commands
          else if (line.match(/^(if|else if|return)/)) color = "#c084fc"; // logic
          else if (line.startsWith("#") || line.startsWith("Set")) color = "#f472b6"; // directives
          else if (line.includes("Run,")) color = "#34d399"; // execution
          return <span key={i} style={{ color }}>{line}{"\n"}</span>;
        })}
      </pre>
    </div>
  );
}

