const updateItemRec = (items, id, updated) => {
  return items.map(item => {
    if (item.id === id) return { ...item, ...updated };
    if (item.children) return { ...item, children: updateItemRec(item.children, id, updated) };
    return item;
  });
};

const deleteItemRec = (items, id) => {
  return items.filter(i => i.id !== id).map(item => {
    if (item.children) return { ...item, children: deleteItemRec(item.children, id) };
    return item;
  });
};

const addItemRec = (items, parentId, newItem) => {
  if (parentId === null) return [...items, newItem];
  return items.map(item => {
    if (item.id === parentId) {
      return { ...item, children: [...(item.children || []), newItem] };
    }
    if (item.children) {
      return { ...item, children: addItemRec(item.children, parentId, newItem) };
    }
    return item;
  });
};

const moveItemRec = (items, id, dir) => {
  const index = items.findIndex(i => i.id === id);
  if (index !== -1) {
    const newIdx = index + dir;
    if (newIdx < 0 || newIdx >= items.length) return items;
    const copy = [...items];
    [copy[index], copy[newIdx]] = [copy[newIdx], copy[index]];
    return copy;
  }
  return items.map(item => {
    if (item.children) {
      return { ...item, children: moveItemRec(item.children, id, dir) };
    }
    return item;
  });
};

function generateAHK(items, hotkey, customHotkey, menuTitle) {
  const hk = hotkey === "custom" ? customHotkey : hotkey;
  let script = `; ═══════════════════════════════════════════════════════
; Custom Context Menu — Generated by AHK Menu Designer
; AutoHotkey v1.1+
; ═══════════════════════════════════════════════════════
#NoEnv
#SingleInstance Force
SetWorkingDir %A_ScriptDir%

; ── Build the menu ──────────────────────────────────────
`;

  let sepCount = 0;
  let subMenuCounter = 0;
  let flatItems = [];

  function processMenu(menuItems, menuName) {
    menuItems.forEach((item) => {
      if (item.type === "separator") {
        sepCount++;
        script += `Menu, ${menuName}, Add  ; separator ${sepCount}\n`;
        return;
      }

      const safeName = item.name.replace(/&/g, "&&") || "Untitled";

      if (item.type === "submenu") {
        subMenuCounter++;
        const subName = `SubMenu${subMenuCounter}`;
        
        if (item.children && item.children.length > 0) {
          processMenu(item.children, subName);
        } else {
          script += `Menu, ${subName}, Add, (Empty), MenuHandler\n`;
          script += `Menu, ${subName}, Disable, (Empty)\n`;
        }
        
        script += `Menu, ${menuName}, Add, ${safeName}, :${subName}\n`;
        if (item.icon && item.icon.trim()) {
          script += `Menu, ${menuName}, Icon, ${safeName}, ${item.icon}\n`;
        }
      } else {
        script += `Menu, ${menuName}, Add, ${safeName}, MenuHandler\n`;
        if (item.icon && item.icon.trim()) {
          script += `Menu, ${menuName}, Icon, ${safeName}, ${item.icon}\n`;
        }
        flatItems.push(item);
      }
    });
  }

  processMenu(items, "QuickMenu");

  script += `
; ── Hotkey ───────────────────────────────────────────────
${hk}::
    Menu, QuickMenu, Show
return

; ── Handler ──────────────────────────────────────────────
MenuHandler:
`;

  const nonSepItems = flatItems.filter((i) => i.type !== "separator");
  nonSepItems.forEach((item, idx) => {
    const safeName = item.name.replace(/&/g, "&&") || "Untitled";
    const condition = idx === 0 ? "if" : "else if";

    if (item.type === "folder") {
      script += `    ${condition} (A_ThisMenuItem = "${safeName}")\n        Run, explorer.exe "${item.target}"\n`;
    } else if (item.type === "url") {
      script += `    ${condition} (A_ThisMenuItem = "${safeName}")\n        Run, ${item.target}\n`;
    } else {
      script += `    ${condition} (A_ThisMenuItem = "${safeName}")\n        Run, "${item.target}"\n`;
    }
  });

  if (nonSepItems.length > 0) {
    script += `return\n`;
  } else {
    script += `    ; No items to handle\nreturn\n`;
  }
  
  return script;
}

